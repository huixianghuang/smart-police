public class FullFlow {
    private int id;
    private String start;
    private String end;
    private int fullFlow;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }

    public int getFullFlow() {
        return fullFlow;
    }

    public void setFullFlow(int fullFlow) {
        this.fullFlow = fullFlow;
    }
}
